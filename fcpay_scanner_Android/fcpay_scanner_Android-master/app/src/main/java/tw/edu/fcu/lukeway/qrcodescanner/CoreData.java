package tw.edu.fcu.lukeway.qrcodescanner;

public class CoreData {

    private String nid;
    private String token;

    public void setNid(String nid) {
        this.nid = nid;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public String getNid() {
        return nid;
    }

    public String getToken() {
        return token;
    }
}
